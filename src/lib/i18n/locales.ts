type Messages = typeof messages.en;

type Path<T, P extends string = ""> = T extends object
    ? {
        [K in keyof T & string]: Path<T[K], P extends "" ? K : `${P}.${K}`>;
    }[keyof T & string]
    : P;

export type TranslationKey = Path<Messages>;

function lookup(dict: unknown, key: string): string | undefined {
    const parts = key.split(".");
    let cursor: unknown = dict;
    for (const p of parts) {
        if (typeof cursor !== "object" || cursor === null) return undefined;
        cursor = (cursor as Record<string, unknown>)[p];
    }
    return typeof cursor === "string" ? cursor : undefined;
}

function interpolate(text: string, vars?: Record<string, string | number>): string {
    if (!vars) return text;
    return text.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
}

export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
    const dict = messages[locale] ?? messages.en;
    const value = lookup(dict, key) ?? lookup(messages.en, key);
    if (value === undefined) return key;
    return interpolate(value, vars);
}

export function detectBrowserLocale(): Locale {
    if (typeof navigator === "undefined") return "en";
    const lang = navigator.language?.toLowerCase() ?? "";
    if (lang.startsWith("de")) return "de";
    return "en";
}

export function isLocale(value: unknown): value is Locale {
    return typeof value === "string" && (SUPPORTED_LOCALES as readonly string[]).includes(value);
}
